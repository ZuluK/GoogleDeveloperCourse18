/*
Functions as parameters
Being able to store a function in a variable makes it really simple to pass the function into another function.
A function that is passed into another function is called a callback.
Let's say you had a helloCat() function,
and you wanted it to return "Hello" followed by a string of "meows" like you had with catSays.
Well, rather than redoing all of your hard work, you can make helloCat() accept a callback function, and pass in catSays.
*/

// function expression catSays
var catSays = function(max) {
  var catMessage = "";
  for (var i = 0; i < max; i++) {
    catMessage += "meow ";
  }
  return catMessage;
};

// function declaration helloCat accepting a callback
function helloCat(callbackFunc) {
  return "Hello " + callbackFunc(3);
}

// pass in catSays as a callback function
helloCat(catSays);
